public class checkMultipleForFive {
    public static void main(String[] args) {
        int n = 10 ;


        if(n%5==0){
            System.out.println("Yes");
        }else{
            System.out.println("no");
        }


        if(n%5==0 && n%7==0){
            System.out.println("Yes");
        }else{
            System.out.println("no");
        }



    }
}
